    // 处理单个导入
    handleSingleImport: function() {
        var idInput = document.getElementById('single-id');
        var numbersInput = document.getElementById('single-numbers');
        
        var id = idInput.value.trim();
        var numbersStr = numbersInput.value.trim();
        
        if (!id || !numbersStr) {
            this.showToast('请填写完整的编号和号码组', 'error');
            return;
        }
        
        // 验证编号是否重复
        if (this.state.numberGroups.some(function(g) { return g.id === id; })) {
            this.showToast('编号已存在，请使用其他编号', 'error');
            return;
        }
        
        try {
            // 解析号码
            var numbers = numbersStr.split(',').map(function(num) {
                var parsedNum = parseInt(num.trim());
                if (isNaN(parsedNum) || parsedNum < 1 || parsedNum > this.config.TOTAL_NUMBERS) {
                    throw new Error('号码格式不正确: ' + num);
                }
                return parsedNum;
            }, this);
            
            // 添加到号码组
            this.addNumberGroup({
                id: id,
                numbers: numbers
            });
            
            // 清空输入
            idInput.value = '';
            numbersInput.value = '';
            
            this.showToast('单个号码组导入成功', 'success');
        } catch (e) {
            this.showToast('导入失败: ' + e.message, 'error');
        }
    },
    
    // 处理批量导入
    handleBatchImport: function() {
        var batchInput = document.getElementById('batch-input');
        var input = batchInput.value.trim();
        
        if (!input) {
            this.showToast('请输入批量数据', 'error');
            return;
        }
        
        var lines = input.split('\n');
        var importedCount = 0;
        var failedCount = 0;
        
        lines.forEach(function(line) {
            line = line.trim();
            if (!line) return;
            
            try {
                // 匹配格式: 编号 号码1,号码2,号码3...
                var parts = line.split(/\s+/);
                if (parts.length < 2) {
                    throw new Error('格式错误');
                }
                
                var id = parts[0];
                var numbersStr = parts.slice(1).join(' ');
                
                // 验证编号是否重复
                if (this.state.numberGroups.some(function(g) { return g.id === id; })) {
                    throw new Error('编号重复: ' + id);
                }
                
                // 解析号码
                var numbers = numbersStr.split(',').map(function(num) {
                    var parsedNum = parseInt(num.trim());
                    if (isNaN(parsedNum) || parsedNum < 1 || parsedNum > this.config.TOTAL_NUMBERS) {
                        throw new Error('号码格式不正确: ' + num);
                    }
                    return parsedNum;
                }, this);
                
                // 添加到号码组
                this.addNumberGroup({
                    id: id,
                    numbers: numbers
                });
                
                importedCount++;
            } catch (e) {
                console.error('导入失败 (行: ' + line + '):', e);
                failedCount++;
            }
        }, this);
        
        // 清空输入
        batchInput.value = '';
        
        if (importedCount > 0) {
            this.showToast('批量导入完成: 成功 ' + importedCount + ' 个，失败 ' + failedCount + ' 个', 'success');
        } else {
            this.showToast('批量导入失败，所有行都有错误', 'error');
        }
    },
    
    // 添加号码组
    addNumberGroup: function(group) {
        this.state.numberGroups.unshift({
            id: group.id,
            numbers: group.numbers,
            timestamp: new Date().toISOString()
        });
        
        // 检查是否超过数据库号码组上限
        const maxLimit = this.config.MAX_GROUPS_LIMIT;
        if (this.state.numberGroups.length > maxLimit) {
            // 计算需要删除的数量
            const deleteCount = this.state.numberGroups.length - maxLimit;
            
            // 删除最小编号的号码组（数组末尾的组）
            this.state.numberGroups.splice(maxLimit, deleteCount);
            
            // 如果删除了数据，显示提示
            this.showToast('数据库已超过350组上限，已自动删除最小编号的' + deleteCount + '组数据', 'warning');
        }
        
        // 保存到本地存储
        this.saveToLocalStorage();
        
        // 更新显示
        this.updateLatestGroupsDisplay();
        this.updateDataCount();
        
        // 如果有选中的号码，重新执行匹配
        if (this.state.selectedNumbers.length > 0) {
            this.performMatch();
        }
    },
    
