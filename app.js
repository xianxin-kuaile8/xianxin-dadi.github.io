    handleDownloadNumberGroups: function() {
        if (this.state.numberGroups.length === 0) {
            this.showToast('没有数据可导出', 'warning');
            return;
        }
        
        // 准备要导出的号码组数据，只提取id和numbers字段
        var numberGroups = this.state.numberGroups.map(function(group) {
            return {
                id: group.id,
                numbers: group.numbers
            };
        });
        
        // 手动构建JSON字符串，确保格式与app.js内部使用的格式完全一致
        // 包括正确的缩进和换行格式
        var jsonLines = ['{\n    "numberGroups": ['];
        
        numberGroups.forEach(function(group, index) {
            jsonLines.push('        {');
            jsonLines.push('            "id": "' + group.id + '",');
            jsonLines.push('            "numbers": [' + group.numbers.join(', ') + ']');
            
            // 如果是最后一个元素，不加逗号
            if (index === numberGroups.length - 1) {
                jsonLines.push('        }');
            } else {
                jsonLines.push('        },');
            }
        });
        
        jsonLines.push('    ]\n}');
        var jsonStr = jsonLines.join('\n');
        
        var blob = new Blob([jsonStr], { type: 'application/json' });
        var url = URL.createObjectURL(blob);
        
        var link = document.createElement('a');
        link.href = url;
        link.download = 'numberGroups_' + new Date().toISOString().split('T')[0] + '.json';
        document.body.appendChild(link);
        link.click();
        document.body.removeChild(link);
        
        URL.revokeObjectURL(url);
        this.showToast('号码组数据已导出，格式与app.js内部使用的格式一致', 'success');
    },
    
    // 处理下载更新后app.js文件
    handleDownloadApp: function() {
        if (this.state.numberGroups.length === 0) {
            this.showToast('没有数据可嵌入到app.js文件中', 'warning');
            return;
        }
        
        // 使用XMLHttpRequest获取当前app.js文件的内容
        var xhr = new XMLHttpRequest();
        // 使用当前页面URL并修改为指向app.js，避免相对路径问题
        var appJsUrl = window.location.href.replace(/[^/]*$/, 'app.js');
        xhr.open('GET', appJsUrl, true);
        xhr.onload = function() {
            if (xhr.status === 200) {
                var appContent = xhr.responseText;
                
                // 准备要嵌入的号码组数据（格式化为JSON字符串）
                var embeddedData = JSON.stringify({
                    numberGroups: this.state.numberGroups
                }, null, 2);
                
                // 替换loadDefaultData方法中的默认数据
                // 查找loadDefaultData方法中的defaultData定义部分
                var defaultDataPattern = /var defaultData = \{[\s\S]*?\};\s*this\.state\.numberGroups = defaultData\.numberGroups;/;
                var replacement = 'var defaultData = ' + embeddedData + ';\n                this.state.numberGroups = defaultData.numberGroups;';
                
                var updatedContent = appContent.replace(defaultDataPattern, replacement);
                
                // 创建Blob对象并提供下载
                var blob = new Blob([updatedContent], { type: 'application/javascript' });
                var url = URL.createObjectURL(blob);
                
                var link = document.createElement('a');
                link.href = url;
                link.download = 'app_' + new Date().toISOString().split('T')[0] + '.js';
                document.body.appendChild(link);
                link.click();
                document.body.removeChild(link);
                
                URL.revokeObjectURL(url);
                this.showToast('app.js文件已生成并开始下载', 'success');
            } else {
                this.showToast('获取app.js文件内容失败', 'error');
            }
        }.bind(this);
        xhr.onerror = function() {
            this.showToast('获取app.js文件内容时发生网络错误', 'error');
        }.bind(this);
        xhr.send();
    },
    
    // 处理导入JSON文件
    handleImportJsonFile: function(event) {
        var file = event.target.files[0];
        if (!file) return;
        
        var reader = new FileReader();
        reader.onload = function(e) {
            try {
                var data = JSON.parse(e.target.result);
                
                // 验证数据格式
                if (!data || !Array.isArray(data.numberGroups)) {
                    throw new Error('数据格式不正确，缺少numberGroups数组');
                }
                
                // 验证每个号码组
                var validGroups = data.numberGroups.filter(function(group) {
                    return group && group.id && Array.isArray(group.numbers);
                });
                
                if (validGroups.length === 0) {
                    throw new Error('没有有效的号码组数据');
                }
                
                // 合并数据，避免重复
                var existingIds = this.state.numberGroups.map(function(g) { return g.id; });
                var newGroups = validGroups.filter(function(group) {
                    return !existingIds.includes(group.id);
                });
                
                // 添加新的号码组
                newGroups.forEach(function(group) {
                    this.state.numberGroups.unshift({
                        id: group.id,
                        numbers: group.numbers,
                        timestamp: group.timestamp || new Date().toISOString()
                    });
                }, this);
                
                // 保存并更新
                this.saveToLocalStorage();
                this.updateLatestGroupsDisplay();
                this.updateDataCount();
                
                this.showToast('成功导入 ' + newGroups.length + ' 个新号码组', 'success');
                
                // 清空文件输入
                event.target.value = '';
                
                // 如果有选中的号码，重新执行匹配
                if (this.state.selectedNumbers.length > 0) {
                    this.performMatch();
                }
            } catch (e) {
                this.showToast('JSON导入失败: ' + e.message, 'error');
                event.target.value = '';
            }
        }.bind(this);
        
        reader.readAsText(file);
    },
    
